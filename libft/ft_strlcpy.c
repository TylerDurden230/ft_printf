#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t srcsize;

	if (!dst || !src)
		return (0);
	else if (dstsize == 0)
		return (ft_strlen(src));
	i = 0;
	srcsize = ft_strlen(src);
	{
		while (src[i] != '\0' && i < (dstsize - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (srcsize);
}
